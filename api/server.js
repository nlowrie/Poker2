// Audio transcription endpoint for system audio
app.post('/api/transcribe-audio', async (req, res) => {
  try {
    const { audioData, audioFormat, source } = req.body;

    if (!audioData) {
      return res.status(400).json({ 
        error: 'Audio data is required',
        code: 'MISSING_AUDIO_DATA'
      });
    }

    if (!process.env.OPENAI_API_KEY) {
      return res.status(500).json({ 
        error: 'OpenAI API key not configured',
        code: 'MISSING_API_KEY'
      });
    }

    console.log(`ğŸ§ Transcribing ${source || 'unknown'} audio...`);
    console.log(`ğŸ“Š Audio format: ${audioFormat || 'webm'}`);

    // Convert base64 to buffer
    const audioBuffer = Buffer.from(audioData, 'base64');
    
    // Create a temporary file-like object for OpenAI
    const audioFile = {
      buffer: audioBuffer,
      name: `audio.${audioFormat || 'webm'}`,
      type: audioFormat === 'webm' ? 'audio/webm' : 'audio/wav'
    };

    console.log('ğŸ¤– Sending audio to OpenAI Whisper...');

    // Use OpenAI client for transcription
    const openai = new OpenAI({
      apiKey: process.env.OPENAI_API_KEY,
    });

    // Create a ReadableStream from the buffer
    const stream = new ReadableStream({
      start(controller) {
        controller.enqueue(audioBuffer);
        controller.close();
      }
    });

    const transcription = await openai.audio.transcriptions.create({
      file: new File([audioBuffer], audioFile.name, { type: audioFile.type }),
      model: 'whisper-1',
      language: 'en',
      response_format: 'json'
    });

    console.log('âœ… Audio transcription successful');
    console.log(`ğŸ“ Transcribed text: "${transcription.text?.substring(0, 100)}..."`);

    res.json({
      transcription: transcription.text || '',
      source: source || 'unknown',
      audioFormat: audioFormat || 'webm',
      timestamp: new Date().toISOString()
    });

  } catch (error) {
    console.error('âŒ Audio transcription error:', error);
    
    if (error.message?.includes('API key')) {
      res.status(401).json({
        error: 'Invalid or missing OpenAI API key',
        code: 'INVALID_API_KEY',
        message: error.message
      });
    } else if (error.message?.includes('quota')) {
      res.status(429).json({
        error: 'OpenAI API quota exceeded',
        code: 'QUOTA_EXCEEDED',
        message: error.message
      });
    } else {
      res.status(500).json({
        error: 'Audio transcription failed',
        code: 'TRANSCRIPTION_ERROR',
        message: error.message
      });
    }
  }
});

